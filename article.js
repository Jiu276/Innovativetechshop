// Get article ID from URL parameter
function getArticleId() {
    const params = new URLSearchParams(window.location.search);
    return params.get('id') || 1; // Default to article 1 if no ID provided
}

// Load article content
function loadArticle(articleId) {
    const article = articlesDatabase[articleId];
    
    if (!article) {
        // Article not found
        document.getElementById('articleContent').innerHTML = '<h2>Article Not Found</h2><p>Sorry, the requested article could not be found.</p>';
        return;
    }
    
    // Update page title
    document.title = `${article.title} - GreenTechLife`;
    
    // Update breadcrumb
    document.getElementById('breadcrumbTitle').textContent = article.title;
    
    // Update hero section
    document.getElementById('heroImage').src = article.image;
    document.getElementById('articleCategory').textContent = article.category;
    document.getElementById('articleTitle').textContent = article.title;
    document.getElementById('articleDate').textContent = article.date;
    document.getElementById('articleAuthor').textContent = article.author;
    document.getElementById('authorName').textContent = article.author;
    document.getElementById('readTime').textContent = `${article.readTime} read`;
    document.getElementById('viewCount').textContent = `${article.views.toLocaleString()} views`;
    
    // Update article content
    document.getElementById('articleContent').innerHTML = article.content;
    
    // Update tags
    const tagsContainer = document.querySelector('.article-tags');
    tagsContainer.innerHTML = '';
    article.tags.forEach(tag => {
        const tagElement = document.createElement('a');
        tagElement.href = '#';
        tagElement.className = 'article-tag';
        tagElement.textContent = `#${tag}`;
        tagsContainer.appendChild(tagElement);
    });
    
    // Load related articles
    loadRelatedArticles(article.relatedArticles);
    
    // Update share buttons
    updateShareButtons(article.title);
    
    // Scroll to top
    window.scrollTo(0, 0);
}

// Load related articles
function loadRelatedArticles(relatedIds) {
    const container = document.getElementById('relatedArticles');
    container.innerHTML = '';
    
    relatedIds.forEach(id => {
        const relatedArticle = articlesDatabase[id];
        if (relatedArticle) {
            const card = document.createElement('div');
            card.className = 'related-card';
            card.onclick = () => {
                window.location.href = `article.html?id=${id}`;
            };
            
            card.innerHTML = `
                <div class="related-image">
                    <img src="${relatedArticle.image}" alt="${relatedArticle.title}">
                </div>
                <div class="related-content">
                    <h3>${relatedArticle.title}</h3>
                    <div class="related-meta">
                        <span>${relatedArticle.date}</span> · <span>${relatedArticle.readTime} read</span>
                    </div>
                </div>
            `;
            
            container.appendChild(card);
        }
    });
}

// Update share buttons with current article URL
function updateShareButtons(title) {
    const url = encodeURIComponent(window.location.href);
    const encodedTitle = encodeURIComponent(title);
    
    document.querySelector('.share-btn.facebook').href = `https://www.facebook.com/sharer/sharer.php?u=${url}`;
    document.querySelector('.share-btn.twitter').href = `https://twitter.com/intent/tweet?url=${url}&text=${encodedTitle}`;
    document.querySelector('.share-btn.linkedin').href = `https://www.linkedin.com/shareArticle?mini=true&url=${url}&title=${encodedTitle}`;
    document.querySelector('.share-btn.email').href = `mailto:?subject=${encodedTitle}&body=${url}`;
}

